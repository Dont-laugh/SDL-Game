	Map::Map()
	{
		m_Dirt = TextureManager::LoadTexture("assets/Dirt.png");
		m_Grass = TextureManager::LoadTexture("assets/Grass.png");
		m_Water = TextureManager::LoadTexture("assets/Water.png");

		Load(level1);

		m_Src.x = m_Src.y = m_Dest.x = m_Dest.y = 0;
		m_Src.w = m_Src.h = m_Dest.w = m_Dest.h = Map::unit;
	}

	Map::~Map()
	{
		TextureManager::DestroyTexture(m_Dirt);
		TextureManager::DestroyTexture(m_Grass);
		TextureManager::DestroyTexture(m_Water);
	}

	void Map::Load(int arr[20][25])
	{
		for (int row = 0; row < 20; ++row)
		{
			for (int col = 0; col < 25; ++col)
			{
				m_Map[row][col] = arr[row][col];
			}
		}
	}

	void Map::Render()
	{
		auto RenderTile = [&](int type)
		{
			switch (type)
			{
				case 0:
					TextureManager::DrawTexture(m_Water, m_Src, m_Dest);
					break;
				case 1:
					TextureManager::DrawTexture(m_Grass, m_Src, m_Dest);
					break;
				case 2:
					TextureManager::DrawTexture(m_Dirt, m_Src, m_Dest);
					break;
			}
		};

		for (int row = 0; row < 20; ++row)
		{
			for (int col = 0; col < 25; ++col)
			{
				m_Dest.x = col * Map::unit;
				m_Dest.y = row * Map::unit;
				RenderTile(m_Map[row][col]);
			}
		}
	}
